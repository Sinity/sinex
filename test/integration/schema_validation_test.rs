#[sinex_test]
async fn test_malformed_event_detection(ctx: TestContext) -> anyhow::Result<()> {
    let pool = ctx.pool().clone();
    let validator = EventValidator::load_from_db(&pool).await?;

    // Test malformed events generated by the integrity module
    let malformed_events = malformed_detection::generate_malformed_test_events();

    for (i, event) in malformed_events.iter().enumerate() {
        let result = validator.validate(event);
        assert!(
            result.is_err(),
            "Malformed event {} should fail validation",
            i
        );

        let error = result.unwrap_err();
        match event.event_type.as_str() {
            "null_payload" => {
                // Null payload should fail validation
                match error {
                    ValidationError::InvalidType { field, .. } if field == "payload" => (),
                    ValidationError::SecurityValidation(_) => (),
                    _ => panic!(
                        "Expected payload validation error for null payload, got: {:?}",
                        error
                    ),
                }
            }
            "empty_source" => {
                // Empty source should fail validation
                match error {
                    ValidationError::InvalidValue { field, .. } if field == "source" => (),
                    _ => panic!(
                        "Expected source validation error for empty source, got: {:?}",
                        error
                    ),
                }
            }
            "oversized_payload" => {
                // Large payload should be detected
                // Note: This might pass basic validation but should be caught by integrity checks
                println!("Oversized payload validation result: {:?}", error);
            }
            _ => {}
        }
    }

    Ok(())
}

#[sinex_test]
async fn test_schema_constraint_validation(ctx: TestContext) -> anyhow::Result<()> {
    let pool = ctx.pool().clone();
    let validator = EventValidator::load_from_db(&pool).await?;

    // Test filesystem events with missing required fields
    let invalid_filesystem_event = EventBuilder::generic("filesystem", "file.created")
        .payload(json!({
            // Missing required "path" field
            "size": 1024,
            "permissions": "644"
        }))
        .build();

    let result = validator.validate(&invalid_filesystem_event);
    assert!(
        result.is_err(),
        "Event missing required field should fail validation"
    );

    match result.unwrap_err() {
        ValidationError::MissingField { field } => {
            assert_eq!(field, "path", "Should detect missing path field");
        }
        _ => panic!("Expected MissingField error"),
    }

    // Test with invalid field types
    let invalid_type_event = EventBuilder::generic("filesystem", "file.created")
        .payload(json!({
            "path": "/test/file.txt",
            "size": "not_a_number", // Should be numeric
            "permissions": 644
        }))
        .build();

    let result = validator.validate(&invalid_type_event);
    assert!(
        result.is_err(),
        "Event with invalid field type should fail validation"
    );

    Ok(())
}

