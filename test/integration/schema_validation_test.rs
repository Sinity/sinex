#[sinex_test]
async fn test_security_validation_injection_attacks(ctx: TestContext) -> anyhow::Result<()> {
    let pool = ctx.pool().clone();
    let validator = EventValidator::load_from_db(&pool).await?;

    // Test various injection attack payloads
    let malicious_payloads = vec![
        // SQL injection attempts
        json!({
            "path": "/test'; DROP TABLE core.events; --",
            "size": 1024
        }),
        // XSS attempts
        json!({
            "path": "/test/<script>alert('xss')</script>",
            "size": 1024
        }),
        // Path traversal attempts
        json!({
            "path": "../../../etc/passwd",
            "size": 1024
        }),
        // Null byte injection
        json!({
            "path": "/test/file\u{0000}.txt",
            "size": 1024
        }),
        // Control character injection
        json!({
            "path": "/test/file\x01\x02\x03.txt",
            "size": 1024
        }),
        // Unicode normalization attacks
        json!({
            "path": "/test/\u{202e}\u{0301}file.txt",
            "size": 1024
        }),
    ];

    for (i, payload) in malicious_payloads.iter().enumerate() {
        let event = EventBuilder::generic("filesystem", "file.created")
            .payload(payload.clone())
            .build();

        let result = validator.validate(&event);

        // Many of these should be caught by security validation
        match result {
            Err(ValidationError::SecurityValidation(msg)) => {
                println!(
                    "Security validation caught malicious payload {}: {}",
                    i, msg
                );
            }
            Err(other_error) => {
                println!("Other validation error for payload {}: {}", i, other_error);
            }
            Ok(_) => {
                // Some might pass basic validation but should be caught by deeper security checks
                println!(
                    "Malicious payload {} passed basic validation (may be caught by deeper checks)",
                    i
                );
            }
        }
    }

    Ok(())
}

#[sinex_test]
async fn test_schema_validation_performance_under_load(ctx: TestContext) -> anyhow::Result<()> {
    let pool = ctx.pool().clone();
    let validator = EventValidator::load_from_db(&pool).await?;

    // Generate a large number of events to test performance
    let num_events = 1000;
    let mut events = Vec::new();

    for i in 0..num_events {
        let event = EventBuilder::filesystem()
            .path(&format!("/test/performance/file_{}.txt", i))
            .created()
            .size(1024)
            .permissions(0o644)
            .build();
        events.push(event);
    }

    // Measure validation performance
    let start_time = std::time::Instant::now();
    let mut validation_results = Vec::new();

    for event in &events {
        let result = validator.validate(event);
        validation_results.push(result.is_ok());
    }

    let elapsed = start_time.elapsed();
    let events_per_second = num_events as f64 / elapsed.as_secs_f64();

    println!(
        "Validated {} events in {:?} ({:.2} events/sec)",
        num_events, elapsed, events_per_second
    );

    // Verify all valid events passed
    let passed_count = validation_results.iter().filter(|&&passed| passed).count();
    assert_eq!(
        passed_count, num_events,
        "All valid events should pass validation"
    );

    // Performance should be reasonable (adjust threshold as needed)
    assert!(
        events_per_second > 100.0,
        "Validation performance too slow: {:.2} events/sec",
        events_per_second
    );

    Ok(())
}

