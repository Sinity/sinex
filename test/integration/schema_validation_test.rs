/// Test malformed event anomaly detection with various problematic patterns
#[rstest]
#[case::empty_source(
    "", "empty.source", json!({"data": "test"}),
    "empty_source", true, "Should detect empty source field"
)]
#[case::null_bytes(
    "test\0source", "null.byte", json!({"data": "test"}),
    "null_bytes", true, "Should detect null bytes in source"
)]
#[case::oversized_payload(
    "test.source", "oversized", json!({"large_field": "x".repeat(500)}),
    "oversized_payload", true, "Should detect oversized payload"
)]
#[case::valid_event(
    "test.source", "valid", json!({"data": "valid"}),
    "valid_event", false, "Valid event should have no anomalies"
)]
#[sinex_test]
#[traced_test]
async fn test_malformed_event_anomaly_detection(
    ctx: TestContext,
    #[case] source: &str,
    #[case] event_type: &str,
    #[case] payload: serde_json::Value,
    #[case] anomaly_category: &str,
    #[case] should_have_anomalies: bool,
    #[case] description: &str,
) -> color_eyre::eyre::Result<()> {
    let _pool = ctx.pool().clone();

    let event = EventBuilder::generic(source, event_type)
        .payload(payload.clone())
        .build();

    let anomalies = malformed_detection::detect_schema_anomalies(&event);

    tracing::info!(
        "Testing {}: detected {} anomalies",
        description,
        anomalies.len()
    );

    for anomaly in &anomalies {
        tracing::debug!("  Detected anomaly: {}", anomaly);
    }

    // Verify anomaly detection matches expectation
    if should_have_anomalies {
        assert!(
            !anomalies.is_empty(),
            "Expected anomalies for {}, but none detected",
            description
        );

        // Verify specific anomaly types
        match anomaly_category {
            "empty_source" => assert!(
                anomalies.iter().any(|a| a.contains("empty") || a.contains("source")),
                "Should detect empty source anomaly"
            ),
            "null_bytes" => assert!(
                anomalies.iter().any(|a| a.contains("null") || a.contains("byte")),
                "Should detect null byte anomaly"
            ),
            "oversized_payload" => assert!(
                anomalies.iter().any(|a| a.contains("oversized") || a.contains("large")),
                "Should detect oversized payload anomaly"
            ),
            _ => {} // Other categories may have different detection patterns
        }
    } else {
        assert!(
            anomalies.is_empty(),
            "Expected no anomalies for {}, but detected: {:?}",
            description,
            anomalies
        );
    }

    // Create snapshot of anomaly detection results
    ctx.snapshot_json(&format!("anomaly_detection_{}", anomaly_category), &serde_json::json!({
        "source": source,
        "event_type": event_type,
        "payload": payload,
        "anomaly_category": anomaly_category,
        "description": description,
        "should_have_anomalies": should_have_anomalies,
        "detected_anomalies": anomalies,
        "anomaly_count": anomalies.len()
    }));

    Ok(())
}

// Helper function to create deeply nested JSON for testing
fn create_deeply_nested_json(depth: usize) -> Value {
    if depth == 0 {
        json!("leaf")
    } else {
        json!({
            "level": depth,
            "nested": create_deeply_nested_json(depth - 1)
        })
    }
}

// Test schema validation with various event types
#[sinex_test]
async fn test_event_type_specific_validation(ctx: TestContext) -> color_eyre::eyre::Result<()> {
    let pool = ctx.pool().clone();
    let validator = EventValidator::load_from_db(&pool).await?;

    // Test filesystem events
    let filesystem_tests = vec![
        // Valid filesystem event
        (
            EventBuilder::filesystem()
                .path("/test/valid.txt")
                .created()
                .size(1024)
                .build(),
            true,
        ),
        // Invalid filesystem event - missing path
        (
            EventBuilder::generic("filesystem", "file.created")
                .payload(json!({"size": 1024}))
                .build(),
            false,
        ),
        // Invalid filesystem event - invalid size type
        (
            EventBuilder::generic("filesystem", "file.created")
                .payload(json!({"path": "/test.txt", "size": "not_a_number"}))
                .build(),
            false,
        ),
    ];

    for (event, should_pass) in filesystem_tests {
        let result = validator.validate(&event);
        if should_pass {
            assert!(
                result.is_ok(),
                "Valid filesystem event should pass: {:?}",
                result
            );
        } else {
            assert!(result.is_err(), "Invalid filesystem event should fail");
        }
    }

    // Test terminal events
    let terminal_tests = vec![
        // Valid terminal event
        (
            EventBuilder::terminal()
                .command("echo test")
                .success()
                .build(),
            true,
        ),
        // Invalid terminal event - missing command
        (
            EventBuilder::generic("terminal.kitty", "command.executed")
                .payload(json!({"exit_code": 0}))
                .build(),
            false,
        ),
    ];

    for (event, should_pass) in terminal_tests {
        let result = validator.validate(&event);
        if should_pass {
            assert!(
                result.is_ok(),
                "Valid terminal event should pass: {:?}",
                result
            );
        } else {
            assert!(result.is_err(), "Invalid terminal event should fail");
        }
    }

    Ok(())
}
