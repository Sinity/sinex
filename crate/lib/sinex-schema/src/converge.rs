/// Converges each registered table against the live database.
///
/// Designed to be called from `apply()` after `create_tables()`. Idempotent —
/// safe to call on both fresh databases (all columns already exist, no-ops) and
/// existing databases (only new columns are added).
///
/// ## Send-safety
///
/// All sea-query statement data is extracted into `Vec<(String, String)>` before
/// the first `.await`. No `Rc`-based sea-query types cross async boundaries.
pub async fn converge_tables(
    pool: &PgPool,
    tables: &[ConvergibleTable],
) -> Result<(), ApplyError> {
    for ct in tables {
        // Phase 1 (sync): materialize column SQL from the sea-query statement.
        // `TableCreateStatement` is !Send (contains Rc<dyn Iden>), so we must
        // extract everything into owned Strings before the first await.
        // The statement is called once and used for both primary and mirror extraction.
        let (primary_cols, mirror_cols) = {
            let stmt = (ct.statement_fn)();
            let primary = extract_column_sqls(&stmt, ct.meta.schema, ct.meta.name, false);
            let mirror = ct.mirror.as_ref().map(|m| {
                let cols = extract_column_sqls(&stmt, m.schema, m.table, m.skip_extra_generated);
                (m, cols)
            });
            (primary, mirror)
        };
        let declared_foreign_keys: Vec<(String, String)> = ct
            .foreign_keys
            .iter()
            .map(|foreign_key| {
                (
                    foreign_key.name.to_string(),
                    (foreign_key.statement_fn)().to_string(PostgresQueryBuilder),
                )
            })
            .collect();

        // Phase 2 (async): apply DDL using only Send-safe String data.
        // One existence check per table — helpers skip the check since we gate here.
        let primary_exists =
            crate::apply::relation_exists(pool, ct.meta.qualified_name).await?;
        if primary_exists {
            add_missing_columns(pool, ct.meta.schema, ct.meta.name, &primary_cols).await?;
            drop_legacy_columns(pool, ct.meta.schema, ct.meta.name, ct.columns_to_drop).await?;
            converge_named_constraints(pool, ct.meta.schema, ct.meta.name, &ct.named_constraints)
                .await?;
            converge_named_foreign_keys(
                pool,
                ct.meta.schema,
                ct.meta.name,
                &declared_foreign_keys,
            )
            .await?;
        }

        if let Some((mirror, mirror_col_sqls)) = mirror_cols {
            let mirror_exists =
                crate::apply::relation_exists(pool, &format!("{}.{}", mirror.schema, mirror.table))
                    .await?;
            if mirror_exists {
                add_missing_columns(pool, mirror.schema, mirror.table, &mirror_col_sqls).await?;
                drop_legacy_columns(pool, mirror.schema, mirror.table, mirror.columns_to_drop)
                    .await?;
            }
        }
    }

    drop_obsolete_indexes(pool).await?;
    Ok(())
}

/// Reports schema gaps (missing columns, missing named constraints) for use by
/// `diff()`. Does not modify the database.
///
/// Like `converge_tables`, extracts all sea-query data synchronously before
/// any `.await` point.
pub async fn report_column_gaps(
    pool: &PgPool,
    tables: &[ConvergibleTable],
) -> Result<Vec<String>, ApplyError> {
    let mut gaps = Vec::new();

    for ct in tables {
        let qname = ct.meta.qualified_name;

        if !crate::apply::relation_exists(pool, qname).await? {
            continue;
        }

        // Sync: extract column names before first await.
        let declared_cols = {
            let stmt = (ct.statement_fn)();
            extract_column_names(&stmt)
        };

        let existing: HashSet<String> =
            actual_columns(pool, ct.meta.schema, ct.meta.name)
                .await?
                .into_iter()
                .collect();

        for name in &declared_cols {
            if !existing.contains(name) {
                gaps.push(format!("missing {qname}.{name}"));
            }
        }

        let existing_constraints: HashSet<String> =
            actual_named_constraints(pool, ct.meta.schema, ct.meta.name)
                .await?
                .into_iter()
                .collect();

        for nc in &ct.named_constraints {
            if !existing_constraints.contains(nc.name) {
                gaps.push(format!("missing {qname} constraint {}", nc.name));
            }
        }

        let existing_foreign_keys: HashSet<String> =
            actual_named_foreign_keys(pool, ct.meta.schema, ct.meta.name)
                .await?
                .into_iter()
                .collect();

        for foreign_key in &ct.foreign_keys {
            if !existing_foreign_keys.contains(foreign_key.name) {
                gaps.push(format!("missing {qname} foreign key {}", foreign_key.name));
            }
        }
    }

    Ok(gaps)
}
