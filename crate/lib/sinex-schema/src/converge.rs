/// The convergence registry: tables whose columns and named constraints are
/// auto-converged on each `apply()` call.
///
/// Add a table here when its `create_table_statement()` is the single source of
/// truth and you want column additions to propagate automatically to existing
/// databases without manual `ALTER TABLE` statements.
pub fn convergible_tables() -> Result<Vec<ConvergibleTable>, ApplyError> {
    Ok(vec![
        ConvergibleTable {
            meta: find_meta("core.events")?,
            statement_fn: Events::create_table_statement,
            // Named provenance constraints — identified and idempotently recreated
            // by name. Anonymous CREATE TABLE CHECKs (the XOR provenance invariant)
            // are excluded here; they're set-and-forget at table creation.
            named_constraints: vec![
                NamedConstraint {
                    name: "events_anchor_byte_non_negative",
                    expression: "anchor_byte IS NULL OR anchor_byte >= 0",
                },
                NamedConstraint {
                    name: "events_source_event_ids_non_empty",
                    expression: "source_event_ids IS NULL OR cardinality(source_event_ids) > 0",
                },
                NamedConstraint {
                    name: "events_source_event_ids_no_nulls",
                    expression: "source_event_ids IS NULL OR array_position(source_event_ids, NULL) IS NULL",
                },
                NamedConstraint {
                    name: "events_source_material_only_offsets",
                    expression: "source_material_id IS NOT NULL OR (anchor_byte IS NULL AND offset_start IS NULL AND offset_end IS NULL AND offset_kind IS NULL)",
                },
                NamedConstraint {
                    name: "events_material_anchor_required",
                    expression: "source_material_id IS NULL OR anchor_byte IS NOT NULL",
                },
                NamedConstraint {
                    name: "events_offsets_pairing",
                    expression: "(offset_start IS NULL) = (offset_end IS NULL)",
                },
                NamedConstraint {
                    name: "events_offsets_require_kind",
                    expression: "offset_kind IS NULL OR (offset_start IS NOT NULL AND offset_end IS NOT NULL)",
                },
                NamedConstraint {
                    name: "events_offset_order",
                    expression: "offset_start IS NULL OR offset_end IS NULL OR offset_end >= offset_start",
                },
                NamedConstraint {
                    name: "events_temporal_policy_valid",
                    expression: "temporal_policy IS NULL OR temporal_policy IN ('inherit_parent', 'latest_input', 'window_boundary', 'declared_effective')",
                },
                NamedConstraint {
                    name: "events_node_model_valid",
                    expression: "node_model IS NULL OR node_model IN ('transducer', 'windowed', 'scope_reconciler')",
                },
            ],
            foreign_keys: vec![NamedForeignKey {
                name: "events_node_run_id_fkey",
                statement_fn: Events::create_node_run_foreign_key,
            }],
            // node_version was the predecessor to node_run_id.
            columns_to_drop: &["node_version"],
            // audit.archived_events was created with LIKE core.events INCLUDING ALL.
            // LIKE only runs at CREATE TABLE time, so new columns added to core.events
            // must be explicitly propagated to the mirror.
            mirror: Some(MirrorSpec {
                schema: "audit",
                table: "archived_events",
                columns_to_drop: &["node_version"],
                // ts_coided is GENERATED ALWAYS AS in core.events, but must be a plain
                // TIMESTAMPTZ in audit.archived_events (the expression is dropped by
                // create_table_sql()). Skip it here — it already exists from LIKE
                // INCLUDING ALL so ADD COLUMN IF NOT EXISTS would be a no-op anyway.
                // Skipping avoids any edge-case where the expression is re-added.
                skip_extra_generated: true,
            }),
        },
        ConvergibleTable {
            meta: find_meta("core.node_manifests")?,
            statement_fn: NodeManifests::create_table_statement,
            named_constraints: vec![],
            foreign_keys: vec![],
            columns_to_drop: &[],
            mirror: None,
        },
    ])
}

