//! Compile-time database CHECK constraint specifications derived from Rust enums.
//!
//! See `crate/lib/sinex-macros/docs/db_check.md` for the macro grammar.
//!
//! # Architecture
//!
//! A `#[derive(DbCheck)]` enum emits a `const DB_CHECK: DbCheckSpec` plus an
//! `inventory::submit!(DbCheckRegistration { ... })` so the schema-apply engine
//! can iterate all `DbCheck`-derived enums at runtime without naming them
//! individually.
//!
//! # Why versioned constraint names
//!
//! When a rendered-to-DB enum variant is renamed, the DB CHECK constraint
//! attached to its column must be replaced. Sea-query's inline `.check(...)`
//! is lost on first table creation and cannot be re-applied without an
//! explicit `ALTER TABLE … DROP CONSTRAINT … ADD CONSTRAINT …`. We make the
//! constraint name *versioned* — `manifest_type_check_v1`,
//! `manifest_type_check_v2` — so the apply engine can:
//!
//! 1. Drop every older version of the same logical constraint
//!    (`<table>_<column>_check_v*`).
//! 2. Drop the legacy *unversioned* sea-query-generated name
//!    (`<table>_<column>_check`) for first-cut migration.
//! 3. Add the current version.
//!
//! Bumping the `version` attr on the enum is sufficient to ship a rename:
//! no migration file, no manual SQL.

/// One DB CHECK constraint specification.
///
/// Generated by `#[derive(DbCheck)]` from an enum's `Display` rendering.
/// The schema-apply engine consumes these via `inventory::iter::<DbCheckRegistration>`.
#[derive(Debug, Clone, Copy)]
pub struct DbCheckSpec {
    /// PostgreSQL schema name (e.g. `"core"`).
    pub schema: &'static str,
    /// Table name (e.g. `"manifests"`).
    pub table: &'static str,
    /// Column name (e.g. `"manifest_type"`).
    pub column: &'static str,
    /// Version number for the constraint name. Bump to trigger a rename
    /// reconciliation in the apply engine.
    pub version: u32,
    /// Allowed values (in the enum's `Display` rendering) — these become the
    /// `IN (...)` list of the generated CHECK clause.
    pub allowed_values: &'static [&'static str],
    /// Name of the originating Rust enum, for diagnostics.
    pub enum_name: &'static str,
}

impl DbCheckSpec {
    /// Current versioned constraint name, e.g. `"manifest_type_check_v1"`.
    #[must_use]
    pub fn constraint_name(&self) -> String {
        format!("{}_check_v{}", self.column, self.version)
    }

    /// Prefix for matching any (old or current) versioned variant of this
    /// constraint, e.g. `"manifest_type_check_v"`. Used by drift detection
    /// to enumerate every live constraint for this column.
    #[must_use]
    pub fn constraint_name_prefix(&self) -> String {
        format!("{}_check_v", self.column)
    }

    /// Legacy sea-query / PostgreSQL default constraint name that pre-dated
    /// versioning, e.g. `"manifests_manifest_type_check"`. The apply engine
    /// drops this alongside older versioned constraints to migrate cleanly.
    #[must_use]
    pub fn legacy_constraint_name(&self) -> String {
        format!("{}_{}_check", self.table, self.column)
    }

    /// Fully-qualified `schema.table` identifier.
    #[must_use]
    pub fn qualified_table(&self) -> String {
        format!("{}.{}", self.schema, self.table)
    }

    /// Render the CHECK clause body: `column IN ('a', 'b', 'c')`.
    #[must_use]
    pub fn check_clause(&self) -> String {
        let values: Vec<String> = self
            .allowed_values
            .iter()
            .map(|v| format!("'{}'", v.replace('\'', "''")))
            .collect();
        format!("{} IN ({})", self.column, values.join(", "))
    }
}

/// Inventory-collected entry for one DbCheck-derived enum.
///
/// The schema-apply engine iterates `inventory::iter::<DbCheckRegistration>`
/// at apply time to converge every registered CHECK constraint.
pub struct DbCheckRegistration {
    pub spec: &'static DbCheckSpec,
}

inventory::collect!(DbCheckRegistration);

/// Yield every registered `DbCheckSpec` in arbitrary order.
pub fn registered_specs() -> impl Iterator<Item = &'static DbCheckSpec> {
    inventory::iter::<DbCheckRegistration>
        .into_iter()
        .map(|r| r.spec)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn check_clause_quotes_single_quotes() {
        let spec = DbCheckSpec {
            schema: "core",
            table: "t",
            column: "c",
            version: 1,
            allowed_values: &["a", "it's"],
            enum_name: "E",
        };
        assert_eq!(spec.check_clause(), "c IN ('a', 'it''s')");
    }

    #[test]
    fn constraint_names() {
        let spec = DbCheckSpec {
            schema: "core",
            table: "manifests",
            column: "manifest_type",
            version: 1,
            allowed_values: &["ingestor"],
            enum_name: "NodeType",
        };
        assert_eq!(spec.constraint_name(), "manifest_type_check_v1");
        assert_eq!(spec.constraint_name_prefix(), "manifest_type_check_v");
        assert_eq!(
            spec.legacy_constraint_name(),
            "manifests_manifest_type_check"
        );
        assert_eq!(spec.qualified_table(), "core.manifests");
    }
}
