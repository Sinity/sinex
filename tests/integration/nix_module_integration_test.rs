fn assert_no_circular_dependencies(deps: &HashMap<String, Vec<String>>) -> Result<()> {
    use std::collections::HashSet;
    
    for (service, _dependencies) in deps {
        let mut visited = HashSet::new();
        let mut path = Vec::new();
        
        if has_circular_dependency(service, deps, &mut visited, &mut path) {
            return Err(color_eyre::eyre::eyre!(
                "Circular dependency detected involving {}: {:?}", 
                service, path
            ));
        }
    }
    
    Ok(())
}

fn has_circular_dependency(
    service: &str,
    deps: &HashMap<String, Vec<String>>, 
    visited: &mut HashSet<String>,
    path: &mut Vec<String>,
) -> bool {
    if path.contains(&service.to_string()) {
        return true;
    }
    
    if visited.contains(service) {
        return false;
    }
    
    visited.insert(service.to_string());
    path.push(service.to_string());
    
    if let Some(dependencies) = deps.get(service) {
        for dep in dependencies {
            let dep_name = dep.replace(".service", "");
            if has_circular_dependency(&dep_name, deps, visited, path) {
                return true;
            }
        }
    }
    
    path.pop();
    false
}

