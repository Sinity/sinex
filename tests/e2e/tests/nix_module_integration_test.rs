#[sinex_test]
async fn test_nixos_module_options_schema() -> TestResult<()> {
    // Test the NixOS module options schema structure
    let module_options = create_test_module_options_schema();

    // Validate top-level option categories
    let expected_categories = vec!["enable", "database", "nodes", "shell", "logging"];

    for category in expected_categories {
        assert!(
            module_options.as_object().unwrap().contains_key(category),
            "Module should have {} options",
            category
        );

        let option_def = &module_options[category];

        // Each option should have type and description
        if let Some(obj) = option_def.as_object() {
            if !obj.contains_key("_meta") {
                // Skip meta entries
                assert!(obj.get("type").is_some(), "{} should have type", category);
                assert!(
                    obj.get("description").is_some(),
                    "{} should have description",
                    category
                );
            }
        }
    }

    // Validate source-specific options
    let event_source_options = &module_options["nodes"]["options"]["eventSources"]["options"];
    let source_types = vec!["filesystem", "terminal", "desktop", "system"];

    for source_type in source_types {
        let source_config = &event_source_options[source_type];
        let options = source_config["options"]
            .as_object()
            .expect("source options should be defined");
        assert!(
            options.contains_key("enable"),
            "{} source should expose an enable option",
            source_type
        );
    }

    // Validate module options can be serialized
    let _options_json =
        serde_json::to_string(&module_options).expect("Module options should be valid JSON");

    Ok(())
}

