    let module_config = create_test_module_config();

    // Validate configuration structure
    assert!(
        module_config.as_object().unwrap().contains_key("services"),
        "Should have services key"
    );
    assert!(
        module_config["services"].get("sinex").is_some(),
        "Should have sinex service config"
    );

    let sinex_config = &module_config["services"]["sinex"];
    assert!(
        sinex_config.get("enable").is_some(),
        "Should have enable flag"
    );
    assert!(
        sinex_config.get("database").is_some(),
        "Should have database config"
    );
    assert!(
        sinex_config.get("nodes").is_some(),
        "Should have node config"
    );
    assert!(
        sinex_config.get("shell").is_some(),
        "Should have shell helpers config"
    );

    // Validate database configuration
    let db_config = &sinex_config["database"];
    assert!(db_config.get("host").is_some(), "Should have database host");
    assert!(db_config.get("port").is_some(), "Should have database port");
    assert!(db_config.get("name").is_some(), "Should have database name");

    // Validate node event source configuration
    let node_config = &sinex_config["nodes"];
    assert!(
        node_config.get("eventSources").is_some(),
        "Node config should expose event sources"
    );

    let event_sources_config = &node_config["eventSources"];
