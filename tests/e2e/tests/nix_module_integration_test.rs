#[sinex_test]
async fn test_service_dependency_chain() -> TestResult<()> {
    // Test that service dependencies are properly configured
    let dependency_chain = create_test_dependency_chain();

    // Validate dependency ordering
    let postgres_deps = &dependency_chain["postgresql"];
    assert!(
        postgres_deps.is_empty(),
        "PostgreSQL should have no Sinex dependencies"
    );

    let ingestd_deps = &dependency_chain["sinex-ingestd"];
    assert!(
        ingestd_deps.contains(&"postgresql.service".to_string()),
        "ingestd should depend on postgres"
    );

    let watcher_services = [
        "sinex-fs-ingestor-1",
        "sinex-terminal-ingestor-1",
        "sinex-desktop-ingestor-1",
        "sinex-system-ingestor-1",
    ];

    for watcher in watcher_services {
        let deps = &dependency_chain[watcher];
        assert!(
            deps.contains(&"sinex-ingestd.service".to_string()),
            "{} should depend on ingestd",
            watcher
        );
    }

    let gateway_deps = &dependency_chain["sinex-gateway"];
    assert!(
        gateway_deps.contains(&"postgresql.service".to_string()),
        "gateway should depend on postgres"
    );

    // Validate no circular dependencies
    assert_no_circular_dependencies(&dependency_chain)?;

    // Validate dependency chain structure
    let _deps_json =
        serde_json::to_string(&dependency_chain).expect("Dependency chain should be valid JSON");

    Ok(())
}

#[sinex_test]
async fn test_systemd_service_configuration() -> TestResult<()> {
    // Test systemd service configuration details
    let service_config = create_test_systemd_service_config();

    for (service_name, config) in &service_config {
        let service_config_obj = &config["serviceConfig"];

        // Validate security settings
        if let Some(dynamic_user) = service_config_obj.get("DynamicUser") {
            assert_eq!(
                dynamic_user.as_bool().unwrap_or(false),
                false,
                "{} should not use DynamicUser (uses static sinex user)",
                service_name
            );
        }

        // Validate restart policy
        let restart = service_config_obj["Restart"].as_str().unwrap();
        assert!(
            restart == "always" || restart == "on-failure",
            "{} should have appropriate restart policy, got: {}",
            service_name,
            restart
        );

        // Validate user/group
        assert_eq!(
            service_config_obj["User"].as_str().unwrap(),
            "sinex",
            "{} should run as sinex user",
            service_name
        );

        // Validate environment variables are set
        if let Some(environment) = service_config_obj.get("Environment") {
            let env_vars = environment.as_array().unwrap();
            let has_db_url = env_vars
                .iter()
                .any(|var| var.as_str().unwrap().starts_with("DATABASE_URL="));
            assert!(has_db_url, "{} should have DATABASE_URL set", service_name);
        }
    }

    // Validate service configuration structure
    let _config_json =
        serde_json::to_string(&service_config).expect("Service config should be valid JSON");

    Ok(())
}

#[sinex_test]
